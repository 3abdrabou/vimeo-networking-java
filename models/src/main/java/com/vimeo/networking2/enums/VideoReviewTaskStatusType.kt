/**
 * The task status described in a video's note.
 */
enum class VideoReviewTaskStatusType {

    /**
     * The task is closed.
     */
    CLOSED,

    /**
     * The task is open.
     */
    OPEN,

    /**
     * Unknown status for the video review task.
     */
    UNKNOWN
}
